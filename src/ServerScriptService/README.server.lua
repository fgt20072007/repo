- Creating Entities
If you want to create a new entity, first initialize it's data inside of `ReplicatedStorage.DataModules.EntityData` then create a part of any size inside of the Workspace.EntitySpawns and call it the name of your new entity
Make sure the entity data has all of the most important datas listed in the instructions inside of the module script

	**Make sure the name of the folder in the workspace is the same as the one in the modulescript**

	- Creating new Rarities
To create a new rarity go inside of the Rarities module in the DataModules folder stated above and create a new gradient or use a preset one and fill in all of the informations, the Percentage is the probability of that entity spawning on the conveyor belt

- Adding new vending machines
Create a new vending machine object same stucture as the default one, make sure to also fill in it's informations inside of the VendingMachines module in the DataModules folder

Make sure to create all of the desired tools and fill up the vending machine however you want,

The depth of the unlock proximity prompt is not important place it anywhere you want inside of the vending machine folder

	**Make sure the name of the folder in the workspace is the same as the one in the modulescript**

	- Create new lock doors
If you want to create a new lock door then just copy the template you see inside of the workspace and add the code inside of the ServerStorage module

	**Make sure the name of the folder in the workspace is the same as the one in the modulescript**

	- Setup Global Configurations
	Make sure to setup global configurations with all of the informations requested, you can keep default for most of them but make sure to change the GroupId, The eventId and anything else you would like

	- Changing the dev products
	If you want to change the devproducts head to the productIds module and change them there

	- Adding and removing tools + changing it's ids
